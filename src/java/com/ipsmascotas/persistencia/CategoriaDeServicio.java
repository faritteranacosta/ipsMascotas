package com.ipsmascotas.persistencia;

/**
 *
 * @author ESTUDIANTE
 */
public class CategoriaDeServicio {
    private int id;
    private String nombre;
    private String descripcion;

    public CategoriaDeServicio(int id, String nombre, String descripcion) {
        this.id = id;
        this.nombre = nombre;
        this.descripcion = descripcion;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }
    
    
}
